def _write_synctex_gz(content: str) -> Path:
    """Persist a SyncTeX string to a temporary .synctex.gz file."""
    tmp = Path(tempfile.mkdtemp()) / "test.synctex.gz"
    with gzip.open(tmp, "wt", encoding="utf-8") as fp:
        fp.write(content)
    return tmp


class TestSyncTeXBox(unittest.TestCase):
    """Behaviour of the SyncTeXBox dataclass."""

    def test_contains_inside(self):
        box = _M.SyncTeXBox(
            page=1, file_id=1, line=10,
            x=0, y=1000, w=2000, h=500, d=100,
        )
        # A point centred horizontally and vertically inside the box.
        self.assertTrue(box.contains(1, sp_x=1000, sp_y=900))

    def test_contains_outside_page(self):
        box = _M.SyncTeXBox(
            page=1, file_id=1, line=10,
            x=0, y=1000, w=2000, h=500, d=100,
        )
        self.assertFalse(box.contains(2, sp_x=1000, sp_y=900))

    def test_contains_outside_horizontal(self):
        box = _M.SyncTeXBox(
            page=1, file_id=1, line=10,
            x=0, y=1000, w=2000, h=500, d=100,
        )
        self.assertFalse(box.contains(1, sp_x=3000, sp_y=900))

    def test_contains_outside_vertical(self):
        box = _M.SyncTeXBox(
            page=1, file_id=1, line=10,
            x=0, y=1000, w=2000, h=500, d=100,
        )
        # Point below the box (y < y-h, y > y+d). y=1000-500=500 is
        # the top; y=1000+100=1100 is the bottom in SyncTeX-coords.
        self.assertFalse(box.contains(1, sp_x=1000, sp_y=400))
        self.assertFalse(box.contains(1, sp_x=1000, sp_y=1200))

    def test_area_includes_height_and_depth(self):
        box = _M.SyncTeXBox(
            page=1, file_id=1, line=10,
            x=0, y=0, w=10, h=4, d=6,
        )
        # area = w * (h + d) = 10 * 10
        self.assertEqual(box.area(), 100)


class TestSyncTeXIndex(unittest.TestCase):
    """Parsing and reverse-lookup against captured SyncTeX content."""

    def test_parses_input_directory(self):
        path = _write_synctex_gz(SAMPLE_SYNCTEX)
        idx = _M.SyncTeXIndex.from_file(path)
        self.assertEqual(
            idx._inputs[1].name, "hello.tex"
        )
        self.assertEqual(
            idx._inputs[4].name, "preamble.tex"
        )

    def test_parses_box_records(self):
        path = _write_synctex_gz(SAMPLE_SYNCTEX)
        idx = _M.SyncTeXIndex.from_file(path)
        # The sample has 3 valid [ box records on page 1 (plus some
        # ( records with zero size that get filtered out).
        self.assertTrue(len(idx._boxes) >= 2)
        for box in idx._boxes:
            self.assertEqual(box.page, 1)

    def test_reverse_lookup_inside_box_returns_file_and_line(self):
        path = _write_synctex_gz(SAMPLE_SYNCTEX)
        idx = _M.SyncTeXIndex.from_file(path)
        # PDF point (300, 600) on page 1 is well inside the
        # full-page box at file_id=1, line=11.
        result = idx.reverse_lookup(page=1, x_pt=300, y_pt=600)
        self.assertIsNotNone(result)
        path_result, line = result
        self.assertEqual(path_result.name, "hello.tex")
        self.assertEqual(line, 11)

    def test_reverse_lookup_on_nonexistent_page_returns_none(self):
        path = _write_synctex_gz(SAMPLE_SYNCTEX)
        idx = _M.SyncTeXIndex.from_file(path)
        # The sample only has page 1.
        self.assertIsNone(idx.reverse_lookup(page=99, x_pt=100, y_pt=100))

    def test_reads_plain_synctex_too(self):
        # If the file isn't gzipped, the parser falls back to plain
        # open() based on the extension.
        plain = Path(tempfile.mkdtemp()) / "test.synctex"
        plain.write_text(SAMPLE_SYNCTEX)
        idx = _M.SyncTeXIndex.from_file(plain)
        self.assertEqual(idx._inputs[1].name, "hello.tex")


class TestPointConversion(unittest.TestCase):
    """The SyncTeX scaled-points constant is right."""

    def test_sp_per_pt_constant(self):
        # 65536 sp per pt is the canonical TeX/SyncTeX scaling.
        self.assertEqual(_M.SYNCTEX_SP_PER_PT, 65536)


